   public void cargarfecha()
    {
        Calendar c=Calendar.getInstance();
        this.txtfecha.setText(String.valueOf(c.get(Calendar.MONTH)+1)+ "/"+String.valueOf(c.get(Calendar.DAY_OF_MONTH))+"/"+String.valueOf(c.get(Calendar.YEAR)));
    }
    
         
