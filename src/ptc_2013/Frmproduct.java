    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
         int fila = tb.getSelectedRow();
          
          limpiar();
          btnagrega.setEnabled(false);
          btnmo.setEnabled(true);
          btneli.setEnabled(true);
          //Datos

          
        this.txtcod.setText(tb.getValueAt(fila, 0).toString());  
        this.txtprecio.setText(tb.getValueAt(fila, 2).toString()); 
        String nombre = (String) tb.getValueAt(fila,3);
        this.txtdetalle.setText(tb.getValueAt(fila, 4).toString()); 
        this.txtexi.setText(tb.getValueAt(fila, 5).toString()); 
      

        txtnombre.setText(nombre);
       
    }//GEN-LAST:event_tbMouseClicked

