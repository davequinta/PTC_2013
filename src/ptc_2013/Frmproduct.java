    private void btnmoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmoActionPerformed
          Lista item = (Lista)cmbt.getSelectedItem();
        int codigo = Integer.parseInt(item.getValue().toString());
        Mtoporduct obj = new Mtoporduct();
          
obj.setCodigo(Integer.parseInt(txtcod.getText()));
        obj.setExistencia(Integer.parseInt(txtexi.getText()));
        obj.setNombre_producto(txtnombre.getText());
        obj.setDetalle(txtdetalle.getText());
          obj.setId_tipo(codigo);
         obj.setPrecio(Double.parseDouble(txtprecio.getText())); 
        

        if(obj.Modificar())
        {
            JOptionPane.showMessageDialog(this, "Datos Modificados");

            limpiar();
           

        }
        else
        {
            JOptionPane.showMessageDialog(this, "ERROR");
        }
    }//GEN-LAST:event_btnmoActionPerformed

    private void btneliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliActionPerformed
        Mtoporduct obj = new Mtoporduct();
       obj.setCodigo(Integer.parseInt(txtcod.getText()));
        int eliminar = JOptionPane.showConfirmDialog(this,  "¿Esta seguro de querer eliminar el registro?", "Atencion", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (eliminar == 0)
        {
            if (obj.Eliminar())
            {
                JOptionPane.showMessageDialog(this, "Datos eliminados");
                limpiar();
               
            }

        }
    }//GEN-LAST:event_btneliActionPerformed

